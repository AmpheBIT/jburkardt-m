function fx = p08_fx ( x )

%*****************************************************************************80
%
%% P08_FX evaluates cos ( x ) - x.
%
%  Licensing:
%
%    This code is distributed under the GNU LGPL license.
%
%  Modified:
%
%    07 May 2011
%
%  Author:
%
%    John Burkardt
%
%  Parameters:
%
%    Input, real X(*), the point at which F is to be evaluated.
%
%    Output, real FX(*), the value of the function at X.
%
  fx = cos ( x ) - x;

  return
end
