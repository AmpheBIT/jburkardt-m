function value = c8_mul ( z1, z2 )

%*****************************************************************************80
%
%% C8_MUL multiplies two C8's.
%
%  Discussion:
%
%    A C8 is a complex value.
%
%  Licensing:
%
%    This code is distributed under the GNU LGPL license. 
%
%  Modified:
%
%    02 March 2013
%
%  Author:
%
%    John Burkardt
%
%  Parameters:
%
%    Input, complex Z1, Z2, the values to multiply.
%
%    Output, complex VALUE, the function value.
%
  value = z1 * z2;

  return
end
