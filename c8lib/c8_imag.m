function value = c8_imag ( z )

%*****************************************************************************80
%
%% C8_IMAG returns the imaginary part of a C8.
%
%  Discussion:
%
%    A C8 is a complex value.
%
%  Licensing:
%
%    This code is distributed under the GNU LGPL license. 
%
%  Modified:
%
%    02 March 2013
%
%  Author:
%
%    John Burkardt
%
%  Parameters:
%
%    Input, complex Z, the argument.
%
%    Output, complex VALUE, the imaginary part of Z.
%
  value = imag ( z );

  return
end
