function value = r8_sqrt_i4 ( i )

%*****************************************************************************80
%
%% R8_SQRT_I4 returns the square root of an I4 as an R8.
%
%  Discussion:
%
%    In MATLAB, this function doesn't really serve a useful purpose.
%
%  Licensing:
%
%    This code is distributed under the GNU LGPL license. 
%
%  Modified:
%
%    05 June 2012
%
%  Author:
%
%    John Burkardt
%
%  Parameters:
%
%    Input, integer I, the number whose square root is desired.
%
%    Output, real R8_SQRT_I4, the value of sqrt(I).
%
  value = sqrt ( i );

  return
end
