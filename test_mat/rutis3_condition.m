function value = rutis3_condition ( )

%*****************************************************************************80
%
%% RUTIS3_CONDITION returns the L3 condition of the RUTIS1 matrix.
%
%  Licensing:
%
%    This code is distributed under the GNU LGPL license.
%
%  Modified:
%
%    27 January 2015
%
%  Author:
%
%    John Burkardt
%
%  Parameters:
%
%    Output, real VALUE, the L1 condition.
%
  a_norm = 12.0;
  b_norm = 0.5;
  value = a_norm * b_norm;

  return
end
