function z = sincos_f ( x, y )

%*****************************************************************************80
%
%% SINCOS_F evaluates the function z = sin(x) + cos(y).
%
%  Licensing:
%
%    This code is distributed under the GNU LGPL license.
%
%  Modified:
%
%    26 August 2012
%
%  Author:
%
%    John Burkardt
%
%  Parameters:
%
%    Input, real X, Y, the arguments.
%
%    Output, real Z, the function value.
%
  z = sin ( x ) + cos ( y );

  return
end
