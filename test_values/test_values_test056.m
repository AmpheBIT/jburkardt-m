  fprintf ( 1, '\n' );
  fprintf ( 1, 'TEST056:\n' );
  fprintf ( 1, '  DEBYE3_VALUES stores values of \n' );
  fprintf ( 1, '  the Debye function of order 3.\n' );
  fprintf ( 1, '\n' );
  fprintf ( 1, '      X           FX\n' );
  fprintf ( 1, '\n' );

  n_data = 0;

  while ( 1 )

    [ n_data, x, fx ] = debye3_values ( n_data );

    if ( n_data == 0 )
      break
    end

    fprintf ( 1, '  %12f  %24.16f\n', x, fx );

  end

  return
end
