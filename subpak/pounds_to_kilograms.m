function kg = pounds_to_kilograms ( lb )

%*****************************************************************************80
%
%% POUNDS_TO_KILOGRAMS converts a measurement in pounds to kilograms.
%
%  Licensing:
%
%    This code is distributed under the GNU LGPL license.
%
%  Modified:
%
%    24 March 2004
%
%  Author:
%
%    John Burkardt
%
%  Parameters:
%
%    Input, real LB, the weight in pounds.
%
%    Output, real KG, the corresponding weight in kilograms.
%
  kg = 0.4535924 * lb;

  return
end
